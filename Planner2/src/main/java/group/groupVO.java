package group;

public class groupVO {
	private int gno;
	private int bno;
	private String id;
	private String gname;
	private int gmemberType;
	private String gcreateDate;
	public int getGno() {
		return gno;
	}
	public void setGno(int gno) {
		this.gno = gno;
	}
	public int getBno() {
		return bno;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public int getGmemberType() {
		return gmemberType;
	}
	public void setGmemberType(int gmemberType) {
		this.gmemberType = gmemberType;
	}
	public String getGcreateDate() {
		return gcreateDate;
	}
	public void setGcreateDate(String gcreateDate) {
		this.gcreateDate = gcreateDate;
	}
	
	
	
	
}
