package invate;

public class joinVO {
	private int jno;
	private int gno;
	private String recipient;
	private String inviter;
	private String joinType;
	private int joinAlarm;
	private String jCreateDate;
	public int getJno() {
		return jno;
	}
	public void setJno(int jno) {
		this.jno = jno;
	}
	public int getGno() {
		return gno;
	}
	public void setGno(int gno) {
		this.gno = gno;
	}
	public String getRecipient() {
		return recipient;
	}
	public void setRecipient(String recipient) {
		this.recipient = recipient;
	}
	public String getInviter() {
		return inviter;
	}
	public void setInviter(String inviter) {
		this.inviter = inviter;
	}
	public String getJoinType() {
		return joinType;
	}
	public void setJoinType(String joinType) {
		this.joinType = joinType;
	}
	public int getJoinAlarm() {
		return joinAlarm;
	}
	public void setJoinAlarm(int joinAlarm) {
		this.joinAlarm = joinAlarm;
	}
	public String getjCreateDate() {
		return jCreateDate;
	}
	public void setjCreateDate(String jCreateDate) {
		this.jCreateDate = jCreateDate;
	}
	
}
