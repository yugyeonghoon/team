package variable;

public class Example04 {

	public static void main(String[] args) {
		//문자열 
		//자바에서 문자열은 쌍따옴표 사이에 감싸서 사용해야한다.
		//공백, 띄어쓰기, 빈문자, 한 단어도 전부 문자열로 취급한다.
		String s;
		s = "헬로우";
		System.out.println(s);
		
		s= "h";
		System.out.println(s);
		
		s= "";
		System.out.println(s);
		
		s = " ";
		System.out.println(s);
		
		System.out.println("-------");
		
	}

}
